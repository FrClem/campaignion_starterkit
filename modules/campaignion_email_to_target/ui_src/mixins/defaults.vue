<script>
  export default {

  methods: {

    validSpecificationTypes() {
      return ['message-template', 'exclusion']
    },

    defaultMessageObj() {
      return {
        subject: '',
        header: '',
        body: '',
        footer: ''
      }
    },

    emptySpec(type) {
      if (this.validSpecificationTypes().indexOf(type) === -1) return
      var spec = {
        type: type,
        label: '',
        filters: [],
        filterStr: '', // Verbal expression of a specificationâ€™s filters
        errors: []
      }
      if (type == 'message-template') spec.message = this.defaultMessageObj()
      return spec
    }

  }

}
</script>
